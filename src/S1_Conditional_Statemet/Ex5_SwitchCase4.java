package S1_Conditional_Statemet;
public class Ex5_SwitchCase4
{
    public static void main(String[] args)
    {
        switch ('A')
        {
            case 'A':
                System.out.println("running grade A");
                break;
            case 'B':
                System.out.println("running grade B");
                break;
            case 'C':
                System.out.println("running grade C");
                break;
            case 'D':
                System.out.println("running grade D");
                break;
            case 'E':
                System.out.println("running grade E");
                break;
            default:
                System.out.println("Wrong Input");
                break;
        }

        System.out.println("hi");
    }
}
