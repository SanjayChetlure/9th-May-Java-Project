package S1_Loops;
public class forLoop5_printNum_from_5_to_1
{
    public static void main(String[] args) {

        //start num: 5
        //end num: 1
        //incr/decr: dect by 1

        //           0>=1      //4 3 2 1 0
        for(int i=5; i>=1; i--)
        {
            System.out.println(i);       //5 4 3 2 1
        }
    }
}
